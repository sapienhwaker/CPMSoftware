    private void add(String name, int level) {

        mainActivityList.setModel(projectsMainModel);
        mainActivityList.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        if(level == 1)
            projectsMainModel.addElement("*"+name);
        else
            projectsMainModel.addElement(" "+name);
    }

    private void add1(String name) {

        subActivityList.setModel(projectsSubModel);
        subActivityList.setEnabled(false);
        if (flag == true) {
            projectsSubModel.addElement("*" + name);
            flag = false;
        } else {
            projectsSubModel.addElement(" "+name);
        }
    }


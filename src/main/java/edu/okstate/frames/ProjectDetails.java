    private void mainActivityListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mainActivityListMouseClicked
        // TODO add your handling code here:
        System.out.println("MOUSE HAS BEEN CLICKED ONCE IN THE LIST****************************************");
        projectsSubModel.clear();

        int index = mainActivityList.getSelectedIndex();
        INDEX = index;
        System.out.println("[+++++++++++++++++++++++++++++++++++++++++++++++]");
        System.out.println(projectDataAll.get(0).toString());
        //int index1 = subActivityList.getSelectedIndex();

        if (projectDataAll.get(index).getActivityLevel() == 1) {
            do {
                add1(projectDataAll.get(index).getActivityName());
                index = index + 1;

                quantity.setEnabled(false);
                avgProdRate.setEnabled(false);
                duration.setEnabled(false);
                durationOverride.setEnabled(false);

                comments.setEnabled(false);
                techDetails.setEnabled(false);

                unit.setText("--");
                quantity.setText("0.0");
                avgProdRate.setText("0.0");
                duration.setText("0.0");
                durationOverride.setText("0.0");

                comments.setText("");
                techDetails.setText("");

            } while (projectDataAll.get(index).getActivityParent() != 0);
        } else {

            flag = true;

            quantity.setEnabled(true);
            avgProdRate.setEnabled(true);
            duration.setEnabled(true);
            durationOverride.setEnabled(true);

            comments.setEnabled(true);
            techDetails.setEnabled(true);

            unit.setText(projectDataAll.get(index).getActivityUnit());
            quantity.setText(Double.toString(projectDataAll.get(index).getQuantity()));
            avgProdRate.setText(Double.toString(projectDataAll.get(index).getAvgProdRate()));
            duration.setText(Double.toString(projectDataAll.get(index).getDuration()));
            durationOverride.setText(Double.toString(projectDataAll.get(index).getDurationOverride()));

            comments.setText(projectDataAll.get(index).getComments());
            techDetails.setText(projectDataAll.get(index).getTechDetails());
        }
    }//GEN-LAST:event_mainActivityListMouseClicked

